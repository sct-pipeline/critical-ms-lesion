def detect_laterality(image, lesion_mask, sc_mask, discs_levels, output_path, qc_folder):
    """
    This function detects the laterality of the lesions based on the sct_analyze_lesion command.
    Inspiration 
    Input:
        image: Path to the input image (NIfTI format)
        lesion_mask: Path to the lesion mask (NIfTI format)
        sc_mask: Path to the SC segmentation mask (NIfTI format)
        discs_levels: Path to the vertebral labeling (NIfTI format)
        output_path: Path to the output folder
        qc_folder: Path to the quality control folder
    Output:
        A report containing the laterality of each lesion
    """
    # Build template_folder
    template_folder = os.path.join(output_path, "template_folder")
    os.makedirs(template_folder, exist_ok=True)

    # if os.path.exists(output_report):
    #     return output_report

    # Register to template
    registration_param = "step=1,type=imseg,algo=centermassrot,rot_method=hog,iter=10:step=2,type=seg,algo=bsplinesyn,slicewise=1"
    assert os.system(f"sct_register_to_template -i {image} -s {sc_mask} -ldisc {discs_levels} -ref subject -param {registration_param} -c t2 -ofolder {template_folder} -qc {qc_folder}") == 0, "Error running the sct_register_to_template command"
    
    # Warp atlas to the subject space
    path_template = os.path.join(template_folder, "warp_template2anat.nii.gz")
    assert os.system(f"sct_warp_template -d {image} -w {path_template} -ofolder {template_folder} -qc {qc_folder}") == 0, "Error running the sct_warp_template command"

    # Atlas-based lesion analysis
    assert os.system(f"sct_analyze_lesion -m {lesion_mask} -s {sc_mask} -f {template_folder} -perslice 1 -qc {qc_folder}") == 0, "Error running the sct_analyze_lesion command"

    return None


def detect_critical_lesions(input_scan, output_path, pam_50_csa_folder):

    # Build the output folder
    image_name = input_scan.split("/")[-1].replace(".nii.gz", "")
    output_path = os.path.join(output_path, image_name)
    os.makedirs(output_path, exist_ok=True)
    qc_folder = os.path.join(output_path, "qc")
    os.makedirs(qc_folder, exist_ok=True)

    # Placeholder for the actual lesion detection logic
    print(f"Detecting critical lesions in scan: {input_scan}")
    # Copy img to output folder
    assert os.system(f"cp {input_scan} {output_path}") == 0, "Error copying the input scan to the output folder"
    # SC segmentation
    sc_mask = run_sc_segmentation(input_scan, output_path, qc_folder)
    # Vert labeling
    vert_levels =run_vert_labeling(input_scan, output_path, qc_folder)
    # Lesion segmentation
    lesion_mask = run_lesion_segmentation(input_scan, sc_mask, output_path, qc_folder)
    
    # For each lesion, we compute its CoM and size
    lesion_statistics = get_lesion_stats(lesion_mask)

    # Now we investigate the detection of spinal cord atrophy
    norm_csa_file = compute_normalized_csa(sc_mask, vert_levels, output_path, qc_folder)

    # Now we plot the CSA compared to the PAM50
    path_csa_plot = plot_csa(norm_csa_file, pam_50_csa_folder, output_path)

    # Now we perform asymetry computation
    asymetry_csv = compute_asymmetry(input_scan, sc_mask, vert_levels, output_path, qc_folder)

    # Plot asymetry
    path_asymmetry_plot = plot_asymmetry(asymetry_csv, lesion_statistics, output_path)

    # Detect laterality
    laterality_report = detect_laterality(input_scan, lesion_mask, sc_mask, vert_levels, output_path, qc_folder)


