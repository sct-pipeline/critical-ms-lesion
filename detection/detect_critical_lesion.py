def parse_arguments():
    parser = argparse.ArgumentParser(description="Detect critical lesions in MRI scans.")
    parser.add_argument("-i", "--input", required=True, help="Path to the MRI scan (NIfTI format)")
    parser.add_argument("--lesion-seg", help="Path to the lesion segmentation mask (NIfTI format)")
    parser.add_argument("--date-birth", required=True, type=str, help="Date of birth of the subject YYYYMMDD")
    parser.add_argument("--sex", required=True, choices=["M", "F"], help="Sex of the subject (used for atrophy detection)")
    parser.add_argument("--pam50", help="Path to the PAM50 template folder containing all the csv files")
    parser.add_argument("--hc-data", required=True, help="Path to the healthy control data folder for asymmetry comparison")
    parser.add_argument("-o", "--output_folder", required=True, help="Path to the output folder")
    return parser.parse_args()


def run_lesion_segmentation(input_scan, sc_mask, lesion_mask_input, output_path, qc_folder):

    # Build the lesion mask output path
    lesion_mask = os.path.join(output_path, input_scan.split("/")[-1].replace(".nii.gz", "_label-lesion_seg.nii.gz"))

    if lesion_mask_input is not None:
        # Copy the lesion mask input to the lesion mask output path
        os.system(f"cp {lesion_mask_input} {lesion_mask}")
    
    # Run the SCT command
    if os.path.exists(lesion_mask):
        return lesion_mask
    
    assert os.system(f"SCT_USE_GPU=1 sct_deepseg lesion_ms -i {input_scan} -o {lesion_mask} -qc {qc_folder} -qc-seg {sc_mask} -qc-plane Axial" ) == 0, "Error running the lesion segmentation model"

    return lesion_mask


def run_sc_segmentation(input_scan, output_path, qc_folder):

    # Build the SC mask output path
    sc_mask = os.path.join(output_path, input_scan.split("/")[-1].replace(".nii.gz", "_label-SC_seg.nii.gz"))
    
    # Run the SCT command
    if os.path.exists(sc_mask):
        return sc_mask

    assert os.system(f"SCT_USE_GPU=1 sct_deepseg spinalcord -i {input_scan} -o {sc_mask} -qc {qc_folder}") == 0, "Error running the SC segmentation model"

    return sc_mask


def run_vert_labeling(input_scan, output_path, qc_folder):

    # Build the vertebral labeling output path
    out_files = os.path.join(output_path, "out.nii.gz")
    vert_levels = os.path.join(output_path, input_scan.split("/")[-1].replace(".nii.gz", "_label-discs_dlabel.nii.gz"))


    if os.path.exists(vert_levels):
        return vert_levels
    
    # Run the SCT command
    assert os.system(f"SCT_USE_GPU=1 sct_deepseg spine -i {input_scan} -o {out_files} -qc {qc_folder}") == 0, "Error running the vertebral labeling model"

    # Remove the file out_totalspineseg_all.json and out_totalspineseg_all.nii.gz generated by the command
    assert os.system(f"rm {os.path.join(output_path, 'out_totalspineseg_all.json')}") == 0, "Error removing the out_totalspineseg_all.json file"
    assert os.system(f"rm {os.path.join(output_path, 'out_totalspineseg_all.nii.gz')}") == 0, "Error removing the out_totalspineseg_all.nii.gz file"
    
    # Rename the output file out_totalspineseg_discs.nii.gz to the desired name (same for json)
    assert os.system(f"mv {os.path.join(output_path, 'out_totalspineseg_discs.nii.gz')} {vert_levels}") == 0, "Error renaming the out_totalspineseg_discs.nii.gz file"
    assert os.system(f"mv {os.path.join(output_path, 'out_totalspineseg_discs.json')} {vert_levels.replace(".nii.gz", ".json")}") == 0, "Error renaming the out_totalspineseg_discs.json file"

    return vert_levels


def get_lesion_stats(input_lesion_mask_path, sc_mask, image, vert_levels, output_path, qc_folder):
    """
    This function loads the lesion mask and computes the statistics of each lesion (CoM and size).
    Input:
        lesion_mask: Path to the lesion mask (NIfTI format)
    Output:
        A list of dictionaries containing the statistics of each lesion (CoM and size)
    """
    # Build the lesion stats output path
    lesion_stats_path = os.path.join(output_path, "lesion_stats.json")

    # If the lesion stats file already exists, we load it and return it
    if os.path.exists(lesion_stats_path):
        with open(lesion_stats_path, "r") as f:
            lesion_stats = json.load(f)
        return lesion_stats

    # Build a folder where lesions masks will be saved (one mask per lesion)
    one_lesion_per_mask_folder = os.path.join(output_path, "one_lesion_per_mask")
    os.makedirs(one_lesion_per_mask_folder, exist_ok=True)

    # We also build a temp folder to save intermediate files generated by the sct_process_segmentation command
    temp_folder = os.path.join(output_path, "temp_lesion_stats")
    os.makedirs(temp_folder, exist_ok=True)

    # Initialize the output dict
    lesion_stats = []

    # Load the lesion mask array
    all_lesion_mask = nib.load(input_lesion_mask_path)
    lesion_array = all_lesion_mask.get_fdata()

    # Get the unique lesion labels
    s = ndimage.generate_binary_structure(3,3)
    # Label connected components (lesions)
    labeled_lesions, num_lesions = ndimage.label(lesion_array, structure=s)
    # Get voxel volume
    voxel_volume = np.prod(all_lesion_mask.header.get_zooms())

    # Get orientation
    orientation = nib.aff2axcodes(all_lesion_mask.affine)
    IF_axis_index = orientation.index('I') if 'I' in orientation else (orientation.index('S') if 'S' in orientation else None)

    # For each lesion, compute its CoM and size
    for lesion_label in range(1, num_lesions + 1):
        lesion_mask = labeled_lesions == lesion_label
        # Lesion size
        lesion_size = float(np.sum(lesion_mask)*voxel_volume)
        # Lesion CoM in voxel coordinates
        lesion_com = ndimage.center_of_mass(lesion_mask)
        lesion_com = (float(lesion_com[0]), float(lesion_com[1]), float(lesion_com[2]))
        # We compute a list of all ax slices where the lesion is present
        lesion_slices = list(set(np.where(lesion_mask)[IF_axis_index].tolist()))

        # For each lesion, we compute the slices indexes in the PAM-50 template
        ## We save one mask per lesion in the temp folder
        lesion_mask_nifti = nib.Nifti1Image(lesion_mask.astype(np.uint8), all_lesion_mask.affine, all_lesion_mask.header)
        lesion_mask_path = os.path.join(one_lesion_per_mask_folder, input_lesion_mask_path.split("/")[-1].replace(".nii.gz", f"_lesion_{lesion_label}.nii.gz"))
        nib.save(lesion_mask_nifti, lesion_mask_path)
        ## We label the spinal cord:
        assert os.system(f"sct_label_vertebrae -i {image} -s {sc_mask} -discfile {vert_levels} -c t2 -qc {qc_folder} -ofolder {temp_folder}") == 0, "Error running the sct_label_vertebrae command"
        ## We process the lesion segmentation
        sc_mask_labeled = os.path.join(temp_folder, os.path.basename(sc_mask).replace(".nii.gz", "_labeled.nii.gz"))
        output_csv = os.path.join(temp_folder, f"lesion_{lesion_label}_PAM50.csv")

        # If the lesion segmentation spends only one slice, we need to dilate by one slice in S-I axis
        if len(lesion_slices) == 1:
            # dilation is 0x0x1 if dilating index is 2, else it is 0x1x0 if dilating index is 1, else it is 1x0x0 if dilating index is 0
            if IF_axis_index == 2: dilation = "0x0x1"
            elif IF_axis_index == 1: dilation = "0x1x0"
            else: dilation = "1x0x0"
            temp_dilated_lesion_mask = os.path.join(temp_folder, os.path.basename(lesion_mask_path).replace(".nii.gz", "_dilated.nii.gz"))
            assert os.system(f"sct_maths -i {lesion_mask_path} -shape ball -dilate {dilation} -o {temp_dilated_lesion_mask}") == 0, "Error running the sct_maths command to dilate the lesion mask"
            # Then we process the segmentation with the dilated lesion mask to get the PAM50 slices corresponding
            assert os.system(f"sct_process_segmentation -i {temp_dilated_lesion_mask} -vertfile {sc_mask_labeled} -perslice 1 -normalize-PAM50 1 -o {output_csv}") == 0, "Error running the sct_process_segmentation command"
        
        else:
            assert os.system(f"sct_process_segmentation -i {lesion_mask_path} -vertfile {sc_mask_labeled} -perslice 1 -normalize-PAM50 1 -o {output_csv}") == 0, "Error running the sct_process_segmentation command"
        ## Load the output csv file and extract the slices where the lesion is present
        df_lesion = pd.read_csv(output_csv)
        lesion_slices_pam50 = df_lesion[df_lesion["MEAN(area)"] > 0]["Slice (I->S)"].tolist()
        lesion_slices_pam50 = sorted(lesion_slices_pam50)
        lesion_slices_pam50 = list(set(lesion_slices_pam50))
        ## If we had previously dilated the lesion mask, we need to remove some slices from the lesion_slices_pam50
        if len(lesion_slices) == 1:
            # We have to remove the first 1/3 and last 1/3 of the lesion_slices_pam50 to be sure to keep only the slices corresponding to the original lesion mask
            lesion_slices_pam50 = lesion_slices_pam50[len(lesion_slices_pam50)//3: -len(lesion_slices_pam50)//3]
        
        lesion_stats.append({"label": lesion_label, "size": lesion_size, "CoM": lesion_com, "slices": lesion_slices, "slices_pam50": lesion_slices_pam50})

    # Remove the temp folder
    os.system(f"rm -rf {temp_folder}")

    # Save the lesion statistics to a json file
    with open(lesion_stats_path, "w") as f:
        json.dump(lesion_stats, f)

    return lesion_stats


def compute_pam50_normalized_csa(sc_mask, vert_levels, output_path, qc_folder):
    """
    This function computes the spinal cord CSA at each vertebral level and translates to the PAM50 template.

    Input:
        sc_mask: Path to the SC segmentation mask (NIfTI format)
        vert_levels: Path to the vertebral labeling (NIfTI format)
    Output:
        A dictionary containing the atrophy results (mean cross-sectional area at each vertebral level)
    """
    # Build output csv path
    output_csv = os.path.join(output_path, "csa.csv")

    if os.path.exists(output_csv):
        return output_csv

    # Run the SCT command to compute atrophy
    assert os.system(f"sct_process_segmentation -i {sc_mask} -discfile {vert_levels} -perslice 1 -normalize-PAM50 1 -o {output_csv} -qc {qc_folder}") == 0, "Error running the sct_compute_atrophy command"

    return output_csv


def filter_normative_data(df_normative_data, sex, age):
    """
    This functions filters the normative data to keep only the subjects:
        - of the same sex
        - in the same 10-year age group (if nobody in the same 10-year age group, we keep the closest age group (for example if a subject is 71 and nobody is between 70 and 80, we keep the subjects between 60 and 70 or between 80 and 90 depending on which group has the closest mean age to the subject's age))
    Input:
        - df_normative_data: Dataframe containing the normative data for the healthy control group
        - sex
        - age
    Output:
        - df_normative_data_filtered: Dataframe containing the filtered normative data for the healthy control group
        - age_group: the age group that was kept (either the same 10-year age group or the closest one)
    """
    # 1. Filter by sex first
    df_filtered = df_normative_data[df_normative_data['sex'] == sex].copy()
    # 2. Define the target 10-year age group (e.g., 71 -> [70, 80))
    lower_bound = (age // 10) * 10
    upper_bound = lower_bound + 10
    # Attempt to filter for the specific age group
    df_target_group = df_filtered[
        (df_filtered['age'] >= lower_bound) & 
        (df_filtered['age'] < upper_bound)
    ]
    # 3. Fallback logic: If no subjects in the 10th age group
    if df_target_group.empty:
        # Create a helper column to identify decades
        df_filtered['decade'] = (df_filtered['age'] // 10) * 10
        # Calculate the mean age of each available decade group
        group_means = df_filtered.groupby('decade')['age'].mean()
        # Find the decade whose mean age is closest to the subject's age
        closest_decade = (group_means - age).abs().idxmin()
        df_target_group = df_filtered[df_filtered['decade'] == closest_decade]
        age_group = f"{closest_decade}-{closest_decade + 10}"

    else:
        age_group = f"{lower_bound}-{upper_bound}"
    
    return df_target_group.drop(columns=['decade'], errors='ignore'), age_group


def normalize_outside_lesion(df_normative_data, df_sub, lesion_statistics, path_out_csv, asymmetry=False):
    """
    This function normalizes the subject's CSA values with the healthy control group and plots the normalized values.
    The subject's values are already in the PAM50 space. 
    We want to bring the mean and std to that of the HC data but we want to compute the normalization on areas outside of the lesion to avoid normalizing with pathological values.
    Input:
        df_normative_data: Dataframe containing the normative data for the healthy control group
        df_sub: Dataframe containing the subject's data in the PAM50 space
        lesion_statistics: List of dictionaries containing lesion statistics
        path_out_csv: Path to the output CSV file
    Output:
        df_sub_normalized: Dataframe containing the subject's normalized data in the PAM50 space
    """
    # Get the slices corresponding to the lesions in the PAM50 space
    lesion_slices_pam50 = []
    for lesion in lesion_statistics:
        lesion_slices_pam50.extend(lesion["slices_pam50"])
    # Get all the slides of the subject in the PAM50 space
    subject_slices_pam50 = df_sub["Slice (I->S)"].tolist()
    # Build slices to use for normalization by keeping only the slices that are not in the lesion_slices_pam50
    slices_for_normalization = [slice for slice in subject_slices_pam50 if slice not in lesion_slices_pam50]
    
    # We normlaize the following metrics:
    METRICS = ['MEAN(area)', 'MEAN(diameter_AP)', 'MEAN(diameter_RL)', 'MEAN(compression_ratio)', 'MEAN(eccentricity)',
           'MEAN(solidity)']
    
    if asymmetry:
        METRICS = ['MEAN(symmetry_dice_RL)', 'MEAN(symmetry_hausdorff_RL)', 'MEAN(symmetry_difference_RL)',
                    'MEAN(symmetry_dice_AP)', 'MEAN(symmetry_hausdorff_AP)', 'MEAN(symmetry_difference_AP)',
                    'NORM_DIFF(area_quadrant_anterior_left-right)', 'NORM_DIFF(area_quadrant_posterior_left-right)',
                    'NORM_DIFF(area_left-right)']
    
    df_sub_normalized = df_sub.copy()
    # For each metric, we compute the mean and std of the normative data on the slices outside of the lesion but in subject
    for metric in METRICS:
        normative_mean = df_normative_data[df_normative_data["Slice (I->S)"].isin(slices_for_normalization)][metric].mean()
        normative_std = df_normative_data[df_normative_data["Slice (I->S)"].isin(slices_for_normalization)][metric].std()
        # We normalize the subject's metric by bringing the mean and std to that of the normative data
        df_sub_normalized[metric] = (df_sub[metric] - df_sub[metric].mean()) / df_sub[metric].std() * normative_std + normative_mean
    
    # Save the normalized subject data to a new csv file
    df_sub_normalized.to_csv(path_out_csv, index=False)

    return df_sub_normalized


def plot_csa(pam50_norm_csa_file, sex, age, hc_data, lesion_statistics, output_path):
    """
    Detect atrophies by comparing the PAM50-normalized CSA with the PAM50 template.
    Input:
        pam50_norm_csa_file: Path to the PAM50-normalized CSA file
        sex: Sex of the subject (used for atrophy detection)
        age: Age of the subject (used for atrophy detection)
        hc_data: Path to the healthy control data folder
        lesion_statistics: List of dictionaries containing lesion statistics
        output_path: Path to the output folder
    Output:
        A list of detected atrophies
    """
    # Build output plot path
    path_csa_plot = os.path.join(output_path, "csa_plot.png")
    path_csa_plot_normalized = os.path.join(output_path, "csa_plot_normalized.png")

    if os.path.exists(path_csa_plot):
        return path_csa_plot

    # Load the subject normalized CSA data
    df_sub, min_slice_idx, max_slice_idx = load_single_subject_data(pam50_norm_csa_file)   

    # Load spine-generic normative data
    path_HC = hc_data
    path_participants_tsv = os.path.join(hc_data, "participants.tsv")
    df_normative_data = load_normative_data(path_HC, path_participants_tsv, min_slice=min_slice_idx, max_slice=max_slice_idx)
    # Get the subject ID and the nb of subjects
    subject_id = output_path.split("/")[-1]
    # Filter the normative data to keep only the subjects of the same sex and age group
    df_normative_data_filtered, age_group = filter_normative_data(df_normative_data, sex, age)
    number_of_subjects = len(df_normative_data_filtered['participant_id'].unique())

    # Create the plots
    create_lineplot(df_normative_data_filtered, df_sub, subject_id, number_of_subjects, path_csa_plot, lesion_statistics, sex, age_group)

    #### Normalization fo the metrics values with the healthy control group and plot the normalized values
    path_csv_normalized = os.path.join(output_path, "csa_normalized.csv")
    df_sub_normalized = normalize_outside_lesion(df_normative_data_filtered, df_sub, lesion_statistics, path_csv_normalized)

    # Now we create another plot after the normalization
    create_lineplot(df_normative_data_filtered, df_sub_normalized, subject_id, number_of_subjects, path_csa_plot_normalized, lesion_statistics, sex, age_group)

    return path_csa_plot


def compute_asymmetry(image, sc_mask, vert_levels, output_path, qc_folder, pam50_normalization=False):
    """
    This function computes the asymmetry of the spinal cord at each vertebral level.
    Input:
        image: Path to the input image (NIfTI format)
        sc_mask: Path to the SC segmentation mask (NIfTI format)
        vert_levels: Path to the vertebral labeling (NIfTI format)
        output_path: Path to the output folder
    Output:
        output_csv: Path to the output csv file containing the asymmetry results
    """

    # Build output csv path
    if pam50_normalization:
        output_csv = os.path.join(output_path, "asymmetry_normalized_pam50.csv")
    else:
        output_csv = os.path.join(output_path, "asymmetry.csv")

    if os.path.exists(output_csv):
        return output_csv
    
    if pam50_normalization:
        normalization_flag = "-normalize-PAM50 1"
    else:
        normalization_flag = ""

    # Run asymmetry computation
    assert os.system(f"sct_process_segmentation -i {sc_mask} -anat {image} -perslice 1 -qc {qc_folder} -o {output_csv} -discfile {vert_levels} {normalization_flag}") == 0, "Error running the sct_compute_asymmetry command"

    # Add the following columns
    df_asymmetry = pd.read_csv(output_csv)
    # We create the ratio columns for the anterior and posterior quadrants
    df_asymmetry["RATIO(area_quadrant_anterior_left/right)"] = df_asymmetry["MEAN(area_quadrant_anterior_left)"] / df_asymmetry["MEAN(area_quadrant_anterior_right)"]
    df_asymmetry["RATIO(area_quadrant_posterior_left/right)"] = df_asymmetry["MEAN(area_quadrant_posterior_left)"] / df_asymmetry["MEAN(area_quadrant_posterior_right)"]
    # We create the difference columns for the anterior and posterior quadrants
    df_asymmetry["DIFF(area_quadrant_anterior_left-right)"] = df_asymmetry["MEAN(area_quadrant_anterior_left)"] - df_asymmetry["MEAN(area_quadrant_anterior_right)"]
    df_asymmetry["DIFF(area_quadrant_posterior_left-right)"] = df_asymmetry["MEAN(area_quadrant_posterior_left)"] - df_asymmetry["MEAN(area_quadrant_posterior_right)"]
    # We create a normalized difference column for the anterior and posterior quadrants (difference divided by the mean of the two quadrants)
    df_asymmetry["NORM_DIFF(area_quadrant_anterior_left-right)"] = df_asymmetry["DIFF(area_quadrant_anterior_left-right)"] / df_asymmetry["MEAN(area_quadrant_anterior_left)"]
    df_asymmetry["NORM_DIFF(area_quadrant_posterior_left-right)"] = df_asymmetry["DIFF(area_quadrant_posterior_left-right)"] / df_asymmetry["MEAN(area_quadrant_posterior_left)"]
    
    # Save the updated dataframe to the same csv file
    df_asymmetry.to_csv(output_csv, index=False)

    return output_csv


