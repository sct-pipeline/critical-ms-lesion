"""
This script detects critical lesions in MRI scans.
It takes as scan as input and outputs the detected "critical" lesions with their locations and sizes.

Input:
    -i: Path to the MRI scan (NIfTI format)
    --pam50: Path to the PAM50 template folder containing all the csv files
    -o: Path to the output folder

Output:
    None

Author: Pierre-Louis Benveniste
"""
import argparse
import os
from pdb import run
import nibabel as nib
import numpy as np
from scipy import ndimage
from generate_csa_plot import load_normative_data, load_single_subject_data, create_lineplot


def parse_arguments():
    parser = argparse.ArgumentParser(description="Detect critical lesions in MRI scans.")
    parser.add_argument("-i", "--input_scan", required=True, help="Path to the MRI scan (NIfTI format)")
    parser.add_argument("--pam50", required=True, help="Path to the PAM50 template folder containing all the csv files")
    parser.add_argument("-o", "--output_folder", required=True, help="Path to the output folder")
    return parser.parse_args()


def run_lesion_segmentation(input_scan, output_path, run=True):

    # Build the lesion mask output path
    lesion_mask = os.path.join(output_path, input_scan.split("/")[-1].replace(".nii.gz", "_label-lesion_seg.nii.gz"))
    
    # Run the SCT command
    if run:
        assert os.system(f"SCT_USE_GPU=1 sct_deepseg lesion_ms -i {input_scan} -o {lesion_mask}") == 0, "Error running the lesion segmentation model"

    return lesion_mask


def run_sc_segmentation(input_scan, output_path, run=True):

    # Build the SC mask output path
    sc_mask = os.path.join(output_path, input_scan.split("/")[-1].replace(".nii.gz", "_label-SC_seg.nii.gz"))
    
    # Run the SCT command
    if run:
        assert os.system(f"SCT_USE_GPU=1 sct_deepseg spinalcord -i {input_scan} -o {sc_mask}") == 0, "Error running the SC segmentation model"

    return sc_mask


def run_vert_labeling(input_scan, output_path, run=True):

    # Build the vertebral labeling output path
    out_files = os.path.join(output_path, "out.nii.gz")
    vert_levels = os.path.join(output_path, input_scan.split("/")[-1].replace(".nii.gz", "_label-discs_dlabel.nii.gz"))


    if run:
        # Run the SCT command
        assert os.system(f"SCT_USE_GPU=1 sct_deepseg spine -i {input_scan} -o {out_files}") == 0, "Error running the vertebral labeling model"

        # Remove the file out_totalspineseg_all.json and out_totalspineseg_all.nii.gz generated by the command
        assert os.system(f"rm {os.path.join(output_path, 'out_totalspineseg_all.json')}") == 0, "Error removing the out_totalspineseg_all.json file"
        assert os.system(f"rm {os.path.join(output_path, 'out_totalspineseg_all.nii.gz')}") == 0, "Error removing the out_totalspineseg_all.nii.gz file"
        
        # Rename the output file out_totalspineseg_discs.nii.gz to the desired name (same for json)
        assert os.system(f"mv {os.path.join(output_path, 'out_totalspineseg_discs.nii.gz')} {vert_levels}") == 0, "Error renaming the out_totalspineseg_discs.nii.gz file"
        assert os.system(f"mv {os.path.join(output_path, 'out_totalspineseg_discs.json')} {vert_levels.replace(".nii.gz", ".json")}") == 0, "Error renaming the out_totalspineseg_discs.json file"

    return vert_levels


def get_lesion_stats(lesion_mask):
    """
    This function loads the lesion mask and computes the statistics of each lesion (CoM and size).
    Input:
        lesion_mask: Path to the lesion mask (NIfTI format)
    Output:
        A list of dictionaries containing the statistics of each lesion (CoM and size)
    """
    # Initialize the output dict
    lesion_stats = []

    # Load the lesion mask array
    lesion_mask = nib.load(lesion_mask)
    lesion_array = lesion_mask.get_fdata()

    # Get the unique lesion labels
    s = ndimage.generate_binary_structure(3,3)
    # Label connected components (lesions)
    labeled_lesions, num_lesions = ndimage.label(lesion_array, structure=s)
    # Get voxel volume
    voxel_volume = np.prod(lesion_mask.header.get_zooms())

    # For each lesion, compute its CoM and size
    for lesion_label in range(1, num_lesions + 1):
        lesion_mask = labeled_lesions == lesion_label
        lesion_size = float(np.sum(lesion_mask)*voxel_volume)
        lesion_com = ndimage.center_of_mass(lesion_mask)
        lesion_com = (float(lesion_com[0]), float(lesion_com[1]), float(lesion_com[2]))
        lesion_stats.append({"label": lesion_label, "size": lesion_size, "CoM": lesion_com})
    return lesion_stats


def compute_normalized_csa(sc_mask, vert_levels, output_path, run=True):
    """
    This function computes the spinal cord CSA at each vertebral level and normalizes it based on the PAM50 template.

    Input:
        sc_mask: Path to the SC segmentation mask (NIfTI format)
        vert_levels: Path to the vertebral labeling (NIfTI format)
    Output:
        A dictionary containing the atrophy results (mean cross-sectional area at each vertebral level)
    """
    # Build output csv path
    output_csv = os.path.join(output_path, "csa.csv")

    if run:
        # Run the SCT command to compute atrophy
        assert os.system(f"sct_process_segmentation -i {sc_mask} -discfile {vert_levels} -perslice 1 -normalize-PAM50 1 -o {output_csv}") == 0, "Error running the sct_compute_atrophy command"

    return output_csv


def detect_atrophies(norm_csa_file, pam_50_csa_folder, output_path):
    """
    Detect atrophies by comparing the normalized CSA with the PAM50 template.
    Input:
        norm_csa_file: Path to the normalized CSA file
        pam_50_csa_folder: Path to the PAM50 template folder containing all the csv files
        output_path: Path to the output folder
    Output:
        A list of detected atrophies
    """
    # Load spine-generic normative data
    path_HC = os.path.join(pam_50_csa_folder, "spinal_cord")
    path_participants_tsv = os.path.join(pam_50_csa_folder, "participants.tsv")
    df_normative_data, df_spine_generic_min, df_spine_generic_max = load_normative_data(path_HC, path_participants_tsv)

    # Load the normalized CSA data
    df_sub = load_single_subject_data(norm_csa_file, df_spine_generic_min, df_spine_generic_max)   
    number_of_subjects = len(df_normative_data['participant_id'].unique())

    subject_id = output_path.split("/")[-1]

    # Create the plots
    create_lineplot(df_normative_data, df_sub, subject_id, number_of_subjects, output_path) 
    
    return None


def detect_critical_lesions(input_scan, output_path, pam_50_csa_folder):

    # Build the output folder
    sub_name = input_scan.split("/")[-1].split("_")[0]
    session_name = input_scan.split("/")[-1].split("_")[1]
    output_path = os.path.join(output_path, f"{sub_name}_{session_name}")
    os.makedirs(output_path, exist_ok=True)

    # Placeholder for the actual lesion detection logic
    print(f"Detecting critical lesions in scan: {input_scan}")
    # Copy img to output folder
    assert os.system(f"cp {input_scan} {output_path}") == 0, "Error copying the input scan to the output folder"
    # Lesion segmentation
    lesion_mask = run_lesion_segmentation(input_scan, output_path, run=False)
    # SC segmentation
    sc_mask = run_sc_segmentation(input_scan, output_path, run=False)
    # Vert labeling
    vert_levels =run_vert_labeling(input_scan, output_path, run=False)

    # For each lesion, we compute its CoM and size
    lesion_statistics = get_lesion_stats(lesion_mask)

    # Now we investigate the detection of spinal cord atrophy
    norm_csa_file = compute_normalized_csa(sc_mask, vert_levels, output_path, run=False)

    # Now we detect atrophies by comparing with the PAM50 template
    detect_atrophies(norm_csa_file, pam_50_csa_folder, output_path)


if __name__ == "__main__":
    args = parse_arguments()
    detect_critical_lesions(args.input_scan, args.output_folder, args.pam50)