    qc_folder = os.path.join(output_path, "qc")
    os.makedirs(qc_folder, exist_ok=True)

    # Get scan date
    scan_date = datetime.strptime(input_scan.split("/")[-1].split("_")[1].split("-")[1], "%Y%m%d")
    age = int((scan_date - datetime.strptime(date_birth, "%Y%m%d")).days / 365.25)

    # Placeholder for the actual lesion detection logic
    print(f"Detecting critical lesions in scan: {input_scan}")
    # Copy img to output folder
    assert os.system(f"cp {input_scan} {output_path}") == 0, "Error copying the input scan to the output folder"
    # SC segmentation
    sc_mask = run_sc_segmentation(input_scan, output_path, qc_folder)
    # Vert labeling
    vert_levels =run_vert_labeling(input_scan, output_path, qc_folder)
    # Lesion segmentation
    lesion_mask = run_lesion_segmentation(input_scan, sc_mask, lesion_mask_input, output_path, qc_folder)
    
    # For each lesion, we compute its CoM and size
    lesion_statistics = get_lesion_stats(lesion_mask, sc_mask, input_scan, vert_levels,output_path, qc_folder)

    # Now we investigate the detection of spinal cord atrophy
    pam50_normalized_csa_file = compute_pam50_normalized_csa(sc_mask, vert_levels, output_path, qc_folder)

    # Now we plot the CSA compared to the PAM50
    path_csa_plot = plot_csa(pam50_normalized_csa_file, sex, age, path_hc_data, lesion_statistics, output_path)

